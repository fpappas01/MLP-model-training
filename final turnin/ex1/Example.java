
public class Example {
    private PointSDT point;
    private String category;

    public Example(PointSDT point, String category) {
        this.point = point;
        this.category = category;
    }

    public PointSDT getPoint() {
        return point;
    }

    public void setPoint(PointSDT point) {
        this.point = point;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    
    
}
